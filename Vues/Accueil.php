			  <h2>FAQ</h2>
			  <span class="byline">Questions les plus fréquemment posées</span> 
		  </div>

		  <div id="onebox">
			  <h2>Est-il possible de donner ou de recevoir des cours de mathématiques et de sciences avec ITeachYou ?</h2>
			  <p>Sans aucun problème. La plate-forme comprend un tableau de bord interactif qui permet à ses utilisateurs de dessiner en direct des graphiques et autres formes géométriques.</p>
			  <h2>Comment puis-je donner ou recevoir un cours à partir d’un document existant ?</h2>
			  <p>Depuis la plate-forme, il vous est possible de rechercher un document existant, de l’afficher sur votre tableau de bord, et de le modifier en direct.</p>
			  <h2>Comment puis-je accéder à la plate-forme ?</h2>
			  <p>L’accès à la plate-forme de cours est entièrement libre et gratuit. Il vous suffit d’inscrire votre nom d’utilisateur dans l’espace correspondant (cliquez sur « Essayer » et vous verrez !).</p>
			  <h2>Comment puis-je inviter mon élève ou mon professeur à venir me rejoindre ?</h2>
			  <p>Lorsque vous aurez introduit votre nom d’utilisateur dans l’espace correspondant, un lien vous sera communiqué. Il vous suffira de l’envoyer à votre professeur ou à votre élève afin qu’il puisse à son tour accéder à la plate-forme.</p>
			  <h2>Puis-je donner ou recevoir un cours avec plus de deux personnes à la fois ?</h2>
			  <p>Bien sûr. Le nombre d’utilisateurs n’étant pas limité, il vous est possible d’inviter plus de deux personnes à la fois.</p>
