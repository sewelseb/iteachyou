		  <div id="onebox">
			  <p>ITeachYou est une plate-forme internet qui permet à ses utilisateurs de dispenser et de bénéficier de cours particuliers en ligne. Elle met à leur disposition différents outils facilitant la communication et le partage des documents.</p> 
			  <p>Grâce à ce système d’apprentissage à distance, élèves et professeurs bénéficieront de davantage de flexibilité en termes de déplacements et de temps consacrés à chaque cours.</p> 
			  <p>Son utilisation est entièrement gratuite et ne nécessite aucune inscription au préalable.</p> 
			  <p>Vous éprouvez des difficultés dans l’une de vos matières ? Vous êtes spécialiste d’un domaine, faites preuve de pédagogie et souhaitez faire partager vos connaissances ?</p>			
